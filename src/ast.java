abstract class DeclNode extends ASTnode {
    /**
     * Note: a formal decl needs to return a sym
     */
    abstract public SemSym nameAnalysis(SymTable symTab);

    // default version of typeCheck for non-function decls
    public void typeCheck() { }

	abstract public void codeGen();
	abstract public void codeGen2(int offset);
}

class VarDeclNode extends DeclNode {
    public VarDeclNode(TypeNode type, IdNode id, int size) {
        myType = type;
        myId = id;
        mySize = size;
    }

    /**
     * nameAnalysis (overloaded)
     * Given a symbol table symTab, do:
     * if this name is declared void, then error
     * else if the declaration is of a struct type, 
     *     lookup type name (globally)
     *     if type name doesn't exist, then error
     * if no errors so far,
     *     if name has already been declared in this scope, then error
     *     else add name to local symbol table     
     *
     * symTab is local symbol table (say, for struct field decls)
     * globalTab is global symbol table (for struct type names)
     * symTab and globalTab can be the same
     */
    public SemSym nameAnalysis(SymTable symTab) {
        return nameAnalysis(symTab, symTab, 0, false);
    }
    
    public SemSym nameAnalysis(SymTable symTab, SymTable globalTab, int offset, boolean isGlobal) {
        boolean badDecl = false;
        String name = myId.name();
        SemSym sym = null;
        IdNode structId = null;

        if (myType instanceof VoidNode) {  // check for void type
            ErrMsg.fatal(myId.lineNum(), myId.charNum(), 
                         "Non-function declared void");
            badDecl = true;        
        }
        
        if (symTab.lookupLocal(name) != null) {
            ErrMsg.fatal(myId.lineNum(), myId.charNum(), 
                         "Multiply declared identifier");
            badDecl = true;            
        }
        
        if (!badDecl) {  // insert into symbol table
            try {
				sym = new SemSym(myType.type());
                symTab.addDecl(name, sym);
                myId.link(sym);
            } catch (DuplicateSymException ex) {
                System.err.println("Unexpected DuplicateSymException " +
                                   " in VarDeclNode.nameAnalysis");
                System.exit(-1);
            } catch (EmptySymTableException ex) {
                System.err.println("Unexpected EmptySymTableException " +
                                   " in VarDeclNode.nameAnalysis");
                System.exit(-1);
            }
        }
		sym.setGlobal(isGlobal);
		/*if (sym.isGlobal()) {
			System.out.println("I am a global variable");
		} else {
			System.out.println("I am a local variable");
		}*/
        sym.setOffset(offset);
		//System.out.println("my offset is " + sym.getOffset());
        return sym;
    }    

	public void codeGen() {
		if (myId.sym().isGlobal()) {
			Codegen.generate(".data");
			Codegen.generate(".align 2");
			Codegen.generateLabeled("_" + myId.name(), ".space ", "", "4");	
		}
	}

	public void codeGen2(int offset) {
		myId.sym().setOffset(offset + 8);
		//System.out.println("my offset in if statement is" + String.valueOf(offset + 8));
	}
    
    public void unparse(PrintWriter p, int indent) {
        doIndent(p, indent);
        myType.unparse(p, 0);
        p.print(" ");
        p.print(myId.name());
        p.println(";");
    }

    // 3 kids
    private TypeNode myType;
    private IdNode myId;
    private int mySize;  // use value NOT_STRUCT if this is not a struct type

    public static int NOT_STRUCT = -1;
}

class FnDeclNode extends DeclNode {
    public FnDeclNode(TypeNode type,
                      IdNode id,
                      FormalsListNode formalList,
                      FnBodyNode body) {
        myType = type;
        myId = id;
        myFormalsList = formalList;
        myBody = body;
		localVarsSize = body.myDeclList.myDecls.size() * 4;
    }

    /**
     * nameAnalysis
     * Given a symbol table symTab, do:
     * if this name has already been declared in this scope, then error
     * else add name to local symbol table
     * in any case, do the following:
     *     enter new scope
     *     process the formals
     *     if this function is not multiply declared,
     *         update symbol table entry with types of formals
     *     process the body of the function
     *     exit scope
     */
    public SemSym nameAnalysis(SymTable symTab) {
        String name = myId.name();
		
		if (name.equals("main")) {
			ProgramNode.hasMain = true;
		}

        FnSym sym = null;
        
        if (symTab.lookupLocal(name) != null) {
            ErrMsg.fatal(myId.lineNum(), myId.charNum(),
                         "Multiply declared identifier");
        }
        
        else { // add function name to local symbol table
            try {
                sym = new FnSym(myType.type(), myFormalsList.length(), localVarsSize);
				//sym.setLocalVarsSize(localVarsSize);
				//funcs.put(myId.name(), sym);
                symTab.addDecl(name, sym);
                myId.link(sym);
            } catch (DuplicateSymException ex) {
                System.err.println("Unexpected DuplicateSymException " +
                                   " in FnDeclNode.nameAnalysis");
                System.exit(-1);
            } catch (EmptySymTableException ex) {
                System.err.println("Unexpected EmptySymTableException " +
                                   " in FnDeclNode.nameAnalysis");
                System.exit(-1);
            }
        }
        
        symTab.addScope();  // add a new scope for locals and params
        
        // process the formals
        List<Type> typeList = myFormalsList.nameAnalysis(symTab);
        if (sym != null) {
            sym.addFormals(typeList);
        }
        
        myBody.nameAnalysis(symTab); // process the function body
        
        try {
            symTab.removeScope();  // exit scope
        } catch (EmptySymTableException ex) {
            System.err.println("Unexpected EmptySymTableException " +
                               " in FnDeclNode.nameAnalysis");
            System.exit(-1);
        }
        
		//System.out.println("size of local vars is " + localVarsSize);
        return null;
    } 
       
    /**
     * typeCheck
     */
    public void typeCheck() {
        myBody.typeCheck(myType.type());
    }
        
    public void unparse(PrintWriter p, int indent) {
        doIndent(p, indent);
        myType.unparse(p, 0);
        p.print(" ");
        p.print(myId.name());
        p.print("(");
        myFormalsList.unparse(p, 0);
        p.println(") {");
        myBody.unparse(p, indent+4);
        p.println("}\n");
    }

	public void codeGen() {
		currFunc = myId;
		Codegen.generate(".text");
		if (myId.name().equals("main")) {
				Codegen.generate(".globl main");
			Codegen.generateLabeled("main", "", "", "");
			//Codegen.generateLabeled("__start", "", "", "");
		} else {
			Codegen.generateLabeled("_" + myId.name(), "", "", "");
		}


		// prologue
		Codegen.genPush(Codegen.RA);
		Codegen.genPush(Codegen.FP);
		FnSym f = (FnSym) myId.sym();
		Codegen.generate("addu", Codegen.FP, Codegen.SP, String.valueOf(f.getParamSize() + 8));
		Codegen.generate("subu", Codegen.SP, Codegen.SP, String.valueOf(localVarsSize));


		myFormalsList.codeGen();

		myBody.codeGen();

		//epilogue
		String s = "-" + String.valueOf(f.getParamSize()) + "(" + Codegen.FP + ")";
		String s4 = "-" + String.valueOf(f.getParamSize() + 4) + "(" + Codegen.FP + ")";
		Codegen.generate("lw", Codegen.RA, s);
		Codegen.generate("move", Codegen.T0, Codegen.FP);
		Codegen.generate("lw", Codegen.FP, s4);
		Codegen.generate("move", Codegen.SP, Codegen.T0);
		if (myId.name().equals("main")) {
			Codegen.generate("li", Codegen.V0, String.valueOf(10));
			Codegen.generate("syscall");
		} else {
			Codegen.generate("jr", Codegen.RA);
		}

	}

	public void codeGen2(int offset) {}


    // 4 kids
    private TypeNode myType;
    private IdNode myId;
    private FormalsListNode myFormalsList;
    private FnBodyNode myBody;
	public int localVarsSize;
}

class FormalDeclNode extends DeclNode {
    public FormalDeclNode(TypeNode type, IdNode id) {
        myType = type;
        myId = id;
    }

    /**
     * nameAnalysis
     * Given a symbol table symTab, do:
     * if this formal is declared void, then error
     * else if this formal is already in the local symble table,
     *     then issue multiply declared error message and return null
     * else add a new entry to the symbol table and return that Sym
     */

   public SemSym nameAnalysis(SymTable symTab) {
        return nameAnalysis(symTab, 0);
    }

    public SemSym nameAnalysis(SymTable symTab, int offset) {
        String name = myId.name();
        boolean badDecl = false;
        SemSym sym = null;
        
        if (myType instanceof VoidNode) {
            ErrMsg.fatal(myId.lineNum(), myId.charNum(), 
                         "Non-function declared void");
            badDecl = true;        
        }
        
        if (symTab.lookupLocal(name) != null) {
            ErrMsg.fatal(myId.lineNum(), myId.charNum(), 
                         "Multiply declared identifier");
            badDecl = true;
        }
        
        if (!badDecl) {  // insert into symbol table
            try {
                sym = new SemSym(myType.type());
                symTab.addDecl(name, sym);
                myId.link(sym);
            } catch (DuplicateSymException ex) {
                System.err.println("Unexpected DuplicateSymException " +
                                   " in VarDeclNode.nameAnalysis");
                System.exit(-1);
            } catch (EmptySymTableException ex) {
                System.err.println("Unexpected EmptySymTableException " +
                                   " in VarDeclNode.nameAnalysis");
                System.exit(-1);
            }
        }

        sym.setOffset(offset);
		//System.out.println("formal's offset is " + offset);
        return sym;
    }    
    
    public void unparse(PrintWriter p, int indent) {
        myType.unparse(p, 0);
        p.print(" ");
        p.print(myId.name());
    }

	public void codeGen() {}
	public void codeGen2(int offset) {}


    // 2 kids
    private TypeNode myType;
    private IdNode myId;
}



