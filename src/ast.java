class ProgramNode extends ASTnode {
    public ProgramNode(DeclListNode L) {
        myDeclList = L;
		hasMain = false;
		firstGlobal = true;
		fOffset = 0;
		strLitTable = new Hashtable<String, String>();
		//funcs = new Hashtable<String, FnSym>();
    }

    /**
     * nameAnalysis
     * Creates an empty symbol table for the outermost scope, then processes
     * all of the globals, struct defintions, and functions in the program.
     */
    public void nameAnalysis() {
        SymTable symTab = new SymTable();
        myDeclList.nameAnalysis(symTab, true);
    }
    
    /**
     * typeCheck
     */
    public void typeCheck() {
        myDeclList.typeCheck();
    }
    
    public void unparse(PrintWriter p, int indent) {
        myDeclList.unparse(p, indent);
    }

	public void codeGen() {
		myDeclList.codeGen();
	}

    // 1 kid
    private DeclListNode myDeclList;
	public static boolean hasMain;
	public static Hashtable<String, FnSym> funcs;
}

class DeclListNode extends ASTnode {
    public DeclListNode(List<DeclNode> S) {
        myDecls = S;
    }

    /**
     * nameAnalysis
     * Given a symbol table symTab, process all of the decls in the list.
     */
    public void nameAnalysis(SymTable symTab) {
        nameAnalysis(symTab, symTab, false);
    }

	public void nameAnalysis(SymTable symTab, boolean isGlobal) {
		nameAnalysis(symTab, symTab, isGlobal);
	}
    
    /**
     * nameAnalysis
     * Given a symbol table symTab and a global symbol table globalTab
     * (for processing struct names in variable decls), process all of the 
     * decls in the list.
     */    
    public void nameAnalysis(SymTable symTab, SymTable globalTab, boolean isGlobal) {
        for (int i = 0; i < myDecls.size(); i++) {
            if (myDecls.get(i) instanceof VarDeclNode) {
                ((VarDeclNode) myDecls.get(i)).nameAnalysis(symTab, globalTab, fOffset + 8 + i * 4, isGlobal);
            } else {
                myDecls.get(i).nameAnalysis(symTab);
            }
        }
    }    
   

    /**
     * typeCheck
     */
    public void typeCheck() {
        for (DeclNode node : myDecls) {
            node.typeCheck();
        }


    }
    
    public void unparse(PrintWriter p, int indent) {
        Iterator it = myDecls.iterator();
        try {
            while (it.hasNext()) {
                ((DeclNode)it.next()).unparse(p, indent);
            }
        } catch (NoSuchElementException ex) {
            System.err.println("unexpected NoSuchElementException in DeclListNode.print");
            System.exit(-1);
        }
    }

	public void codeGen() {
		for (DeclNode node : myDecls) {
			node.codeGen();
		}
	}

	public void codeGen2(int offset) {
		for (int i = 0; i < myDecls.size(); i++) {
			myDecls.get(i).codeGen2(offset + i * 4);
		}
	}

    // list of kids (DeclNodes)
    public List<DeclNode> myDecls;
}

class FormalsListNode extends ASTnode {
    public FormalsListNode(List<FormalDeclNode> S) {
        myFormals = S;
    }

    /**
     * nameAnalysis
     * Given a symbol table symTab, do:
     * for each formal decl in the list
     *     process the formal decl
     *     if there was no error, add type of formal decl to list
     */
    public List<Type> nameAnalysis(SymTable symTab) {
        List<Type> typeList = new LinkedList<Type>();
        for (int i = 0; i < myFormals.size(); i++) {
            SemSym sym = myFormals.get(i).nameAnalysis(symTab, i * 4);
            if (sym != null) {
                typeList.add(sym.getType());
            }
        }
		fOffset = myFormals.size() * 4;
        return typeList;
    }    
    
    /**
     * Return the number of formals in this list.
     */
    public int length() {
        return myFormals.size();
    }
    
    public void unparse(PrintWriter p, int indent) {
        Iterator<FormalDeclNode> it = myFormals.iterator();
        if (it.hasNext()) { // if there is at least one element
            it.next().unparse(p, indent);
            while (it.hasNext()) {  // print the rest of the list
                p.print(", ");
                it.next().unparse(p, indent);
            }
        } 
    }

	public void codeGen() {
		for (FormalDeclNode node : myFormals) {
			node.codeGen();
		}
	}

    // list of kids (FormalDeclNodes)
    private List<FormalDeclNode> myFormals;
}

